CREATE TABLE students (
  id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  first_name VARCHAR2(50) NOT NULL,
  last_name VARCHAR2(50) NOT NULL,
  email VARCHAR2(100) NOT NULL UNIQUE,
  created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE teachers (
  id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  first_name VARCHAR2(50) NOT NULL,
  last_name VARCHAR2(50) NOT NULL,
  email VARCHAR2(100) NOT NULL UNIQUE
);

CREATE TABLE courses (
  id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  name VARCHAR2(100) NOT NULL,
  description VARCHAR2(255),
  teacher_id NUMBER,
  CONSTRAINT fk_course_teacher FOREIGN KEY (teacher_id) REFERENCES teachers(id) ON DELETE SET NULL
);

CREATE TABLE course_enrollments (
  id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  student_id NUMBER NOT NULL,
  course_id NUMBER NOT NULL,
  enrolled_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT fk_enroll_student FOREIGN KEY (student_id) REFERENCES students(id) ON DELETE CASCADE,
  CONSTRAINT fk_enroll_course FOREIGN KEY (course_id) REFERENCES courses(id) ON DELETE CASCADE
);

CREATE TABLE activities (
  id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  name VARCHAR2(100) NOT NULL,
  category VARCHAR2(50),
  max_points NUMBER(5,2) DEFAULT 100 NOT NULL
);

CREATE TABLE student_activities (
  id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  student_id NUMBER NOT NULL,
  activity_id NUMBER NOT NULL,
  score NUMBER(5,2) CHECK (score >= 0),
  completed_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT fk_student FOREIGN KEY (student_id) REFERENCES students(id) ON DELETE CASCADE,
  CONSTRAINT fk_activity FOREIGN KEY (activity_id) REFERENCES activities(id) ON DELETE CASCADE
);

CREATE INDEX idx_student_activities_student ON student_activities(student_id);
CREATE INDEX idx_student_activities_activity ON student_activities(activity_id);

CREATE OR REPLACE TRIGGER trg_validate_score
BEFORE INSERT OR UPDATE ON student_activities
FOR EACH ROW
DECLARE
  v_max NUMBER(5,2);
BEGIN
  SELECT max_points INTO v_max FROM activities WHERE id = :NEW.activity_id;
  IF :NEW.score > v_max THEN
    RAISE_APPLICATION_ERROR(-20001, 'Score exceeds max points for activity.');
  END IF;
END;
/

CREATE OR REPLACE PROCEDURE add_student_activity(
  p_student_id IN NUMBER,
  p_activity_id IN NUMBER,
  p_score IN NUMBER
)
AS
BEGIN
  INSERT INTO student_activities (student_id, activity_id, score)
  VALUES (p_student_id, p_activity_id, p_score);
  COMMIT;
END;
